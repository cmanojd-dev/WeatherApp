// Function to get cities by state
export function getCitiesByState(stateId) {
  if (cities[stateId]) {
    return cities[stateId];
  } else {
    return [];
  }
}

// Function to find state based on cityId
export function getStateNameByCityId(cityId) {
  if (cityId) {
    const requiredState = states?.filter(state => state?.id === cityId);
    return requiredState?.[0]?.name ?? '';
  } else {
    return '';
  }
}
